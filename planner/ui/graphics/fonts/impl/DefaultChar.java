        private CharacterData characterData;

        private DefaultChar(String character, boolean[][] data) {
            this.characterData = new CharacterData(character, data);
        }

        public boolean[][] getData() {
            return characterData.getData();
        }

        public String getCharacter() {
            return characterData.getCharacterString();
        }

        public static DefaultChar getChar(String character) {
            for (DefaultChar c : DefaultChar.values()) {
                if (c.getCharacter().equals(character)) {
                    return c;
                }
            }
            return null;
        }

        public CharacterData getCharacterData() {
            return characterData;
        }
